import React, { Component } from 'react';
import { Navbar, NavbarBrand, Nav, NavbarToggler, Collapse, NavItem, Jumbotron,
    Button, Modal, ModalHeader, ModalBody,
    Form, FormGroup, Input, Label } from 'reactstrap';
import { NavLink } from 'react-router-dom';


class Header extends Component {
  constructor(props) {
          super(props);

          this.state = {
            isNavOpen: false,
            isModalOpen: false
        };
        this.toggleModal = this.toggleModal.bind(this);
        this.toggleNav = this.toggleNav.bind(this);
        this.handleLogin = this.handleLogin.bind(this);

      }

        toggleNav() {
          this.setState({
            isNavOpen: !this.state.isNavOpen
          });
        }

        toggleModal() {
        this.setState({
          isModalOpen: !this.state.isModalOpen
        });
      }

      handleLogin(event) {
        this.toggleModal();
        alert("Username: " + this.username.value + " Password: " + this.password.value
            + " Remember: " + this.remember.checked);
        event.preventDefault();

    }


      render() {
            return(
                <div>
                    <Navbar dark expand="md">
                        <div className="container">
                            <NavbarToggler onClick={this.toggleNav} />
                            <NavbarBrand className="mr-auto" href="/"><img src='assets/images/logo.png' height="50" width="50" alt='Book Review Club' /></NavbarBrand>
                            <Collapse isOpen={this.state.isNavOpen} navbar>
                                <Nav navbar>
                                <NavItem>
                                    <NavLink className="nav-link"  to='/home'><span className="fa fa-home fa-lg"></span> Home</NavLink>
                                </NavItem>
                                <NavItem>
                                    <NavLink className="nav-link"  to='/book'><span className="fa fa-book fa-lg"></span> Book Reviews</NavLink>
                                </NavItem>
                                <NavItem>
                                    <NavLink className="nav-link" to='/join'><span className="fa fa-cube fa-lg "></span> Join the Club</NavLink>
                                </NavItem>
                                </Nav>
                                <Nav className="ml-auto" navbar>
                                <NavItem>
                                    <Button outline color="white" style={{color: 'white'}} onClick={this.toggleModal} ><span className="fa fa-sign-in fa-lg"></span> Login</Button>
                                </NavItem>
                            </Nav>
                            </Collapse>
                        </div>
                    </Navbar>
                    <Jumbotron >
                        <div className="container">
                            <div className="row row-header">
                            <img src='assets/images/logo.png'  alt='Book Review Club' />
                                <div className="col-12 col-sm-6">
                                <br></br>
                                <br></br>

                                    <h1>The Book Review Club</h1>
                                    <p>Book club for people who want to meet to discuss a book or books that they have read and express their opinions, likes, and dislikes </p>
                                </div>
                            </div>
                        </div>
                    </Jumbotron>
                    <Modal isOpen={this.state.isModalOpen} toggle={this.toggleModal}>
                    <ModalHeader toggle={this.toggleModal}>Login</ModalHeader>
                    <ModalBody>
                    <Form onSubmit={this.handleLogin}>
                            <FormGroup>
                                <Label htmlFor="username">Username</Label>
                                <Input type="text" id="username" name="username"
                                    innerRef={(input) => this.username = input} />
                            </FormGroup>
                            <FormGroup>
                                <Label htmlFor="password">Password</Label>
                                <Input type="password" id="password" name="password"
                                    innerRef={(input) => this.password = input}  />
                            </FormGroup>
                            <FormGroup check>
                                <Label check>
                                    <Input type="checkbox" name="remember"
                                    innerRef={(input) => this.remember = input}  />
                                    Remember me
                                </Label>
                            </FormGroup>
                            <Button type="submit" value="submit" color="primary">Login</Button>
                        </Form>

                    </ModalBody>
                </Modal>
                </div>
            );
        }
    }


export default Header;
